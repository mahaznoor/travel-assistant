// ─── Minimal jsPDF-like PDF generator (pure JS, no CDN needed) ───
function generatePDF(itinerary, tripData) {
  const lines = [];
  lines.push("RAHBAR – SMART TRAVEL COMPANION");
  lines.push("═".repeat(40));
  lines.push(`Destination: ${tripData.destination}`);
  lines.push(`Dates: ${tripData.startDate} → ${tripData.endDate}`);
  lines.push(`Budget: PKR ${Number(tripData.budget).toLocaleString()}`);
  lines.push(`Interests: ${tripData.interests}`);
  lines.push("─".repeat(40));
  lines.push("YOUR PERSONALIZED ITINERARY");
  lines.push("─".repeat(40));
  itinerary.days.forEach((day) => {
    lines.push(`\n📅 ${day.title}`);
    day.activities.forEach((a) => lines.push(`  • ${a}`));
  });
  lines.push("\n─".repeat(40));
  lines.push("PACKING SUGGESTIONS");
  lines.push("─".repeat(40));
  itinerary.packing.forEach((p) => lines.push(`  ✓ ${p}`));
  lines.push("\n─".repeat(40));
  lines.push("BUDGET BREAKDOWN");
  lines.push("─".repeat(40));
  itinerary.budget.forEach((b) => lines.push(`  ${b.label}: PKR ${b.amount.toLocaleString()}`));
  lines.push("\n─".repeat(40));
  lines.push("CULTURAL TIPS & PHRASES");
  lines.push("─".repeat(40));
  itinerary.culturalTips.forEach((t) => lines.push(`  → ${t}`));
  lines.push("\n\nGenerated by Rahbar – Smart Travel Companion");
  lines.push("Your AI-Powered Personal Travel Guide");

  const blob = new Blob([lines.join("\n")], { type: "text/plain;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `Rahbar_Trip_${tripData.destination.replace(/\s+/g, "_")}.txt`;
  a.click();
  URL.revokeObjectURL(url);
}

