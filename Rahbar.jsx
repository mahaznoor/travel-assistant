// ─── PLANNER PAGE ───
function PlannerPage({ onSubmit }) {
  const [form, setForm] = useState({ destination: "", budget: "", startDate: "", endDate: "", travelers: "2", accommodation: "hotel", interests: [] });
  const [errors, setErrors] = useState({});

  const set = (k, v) => setForm(f => ({...f, [k]: v}));
  const toggleInterest = (i) => {
    setForm(f => ({ ...f, interests: f.interests.includes(i) ? f.interests.filter(x=>x!==i) : [...f.interests, i] }));
  };

  const validate = () => {
    const e = {};
    if (!form.destination.trim()) e.destination = "Please enter a destination";
    if (!form.budget || isNaN(form.budget)) e.budget = "Please enter a valid budget";
    if (!form.startDate) e.startDate = "Please select a start date";
    if (!form.endDate) e.endDate = "Please select an end date";
    setErrors(e);
    return Object.keys(e).length === 0;
  };

  const handleSubmit = (e) => {
    e.preventDefault();
    if (validate()) onSubmit({ ...form, interests: form.interests.join(", ") || "General travel" });
  };

  return (
    <div className="planner-page page-enter">
      <div className="planner-container">
        <div className="planner-header">
          <div className="section-tag" style={{margin:"0 auto 1rem", display:"inline-flex"}}>✦ &nbsp; Trip Planner</div>
          <h1 className="section-title" style={{margin:"0 0 0.5rem"}}>Plan Your Perfect Trip</h1>
          <p style={{color:"var(--muted)", fontSize:"0.95rem"}}>Fill in your preferences and Rahbar will generate a personalized itinerary in seconds.</p>
        </div>
        <div className="form-card">
          <form onSubmit={handleSubmit}>
            <div className="form-grid">
              <div className="form-group full">
                <label className="form-label">🌍 Destination</label>
                <input className="form-input" placeholder="e.g. Paris, Istanbul, Dubai, Lahore..." value={form.destination} onChange={e=>set("destination",e.target.value)} />
                {errors.destination && <span style={{fontSize:"0.78rem",color:"var(--terracotta)"}}>{errors.destination}</span>}
              </div>
              <div className="form-group">
                <label className="form-label">💰 Budget (PKR)</label>
                <input className="form-input" type="number" placeholder="e.g. 100000" value={form.budget} onChange={e=>set("budget",e.target.value)} />
                {errors.budget && <span style={{fontSize:"0.78rem",color:"var(--terracotta)"}}>{errors.budget}</span>}
              </div>
              <div className="form-group">
                <label className="form-label">👥 Travelers</label>
                <select className="form-select" value={form.travelers} onChange={e=>set("travelers",e.target.value)}>
                  {["1","2","3","4","5+"].map(n => <option key={n} value={n}>{n} Traveler{n!=="1"?"s":""}</option>)}
                </select>
              </div>
              <div className="form-group">
                <label className="form-label">📅 Start Date</label>
                <input className="form-input" type="date" value={form.startDate} onChange={e=>set("startDate",e.target.value)} />
                {errors.startDate && <span style={{fontSize:"0.78rem",color:"var(--terracotta)"}}>{errors.startDate}</span>}
              </div>
              <div className="form-group">
                <label className="form-label">📅 End Date</label>
                <input className="form-input" type="date" value={form.endDate} onChange={e=>set("endDate",e.target.value)} />
                {errors.endDate && <span style={{fontSize:"0.78rem",color:"var(--terracotta)"}}>{errors.endDate}</span>}
              </div>
              <div className="form-group full">
                <label className="form-label">🏨 Accommodation Type</label>
                <select className="form-select" value={form.accommodation} onChange={e=>set("accommodation",e.target.value)}>
                  {["Hotel","Airbnb / Apartment","Hostel","Resort","Boutique Hotel","Budget Inn"].map(a => <option key={a} value={a.toLowerCase()}>{a}</option>)}
                </select>
              </div>
              <div className="form-group full">
                <label className="form-label">✨ Interests (select all that apply)</label>
                <div className="interest-chips">
                  {INTERESTS.map(i => (
                    <button type="button" key={i} className={`chip${form.interests.includes(i)?" selected":""}`} onClick={()=>toggleInterest(i)}>{i}</button>
                  ))}
                </div>
              </div>
            </div>
            <button type="submit" className="form-submit">
              <span>🚀</span> Generate My Itinerary
            </button>
          </form>
        </div>
      </div>
    </div>
  );
}

