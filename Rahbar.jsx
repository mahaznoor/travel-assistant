// ─── MAIN APP ───
export default function RahbarApp() {
  const [page, setPage] = useState("home");
  const [menuOpen, setMenuOpen] = useState(false);
  const [scrolled, setScrolled] = useState(false);
  const [tripData, setTripData] = useState(null);
  const [itinerary, setItinerary] = useState(null);
  const [loading, setLoading] = useState(false);
  const [loadingMsg, setLoadingMsg] = useState(0);

  const loadingMessages = [
    "Analyzing your travel preferences…",
    "Crafting your personalized itinerary…",
    "Checking weather & local events…",
    "Preparing cultural insights…",
  ];

  useEffect(() => {
    const onScroll = () => setScrolled(window.scrollY > 20);
    window.addEventListener("scroll", onScroll);
    return () => window.removeEventListener("scroll", onScroll);
  }, []);

  useEffect(() => {
    window.scrollTo({ top: 0, behavior: "smooth" });
  }, [page]);

  const navigate = (p) => { setPage(p); setMenuOpen(false); };

  const handlePlanSubmit = (data) => {
    setTripData(data);
    setLoading(true);
    setLoadingMsg(0);
    const interval = setInterval(() => setLoadingMsg(m => (m + 1) % loadingMessages.length), 1800);
    setTimeout(() => {
      clearInterval(interval);
      // Match destination to dataset or use Paris as default
      const key = Object.keys(DESTINATIONS).find(k => data.destination.toLowerCase().includes(k.toLowerCase())) || "Paris";
      setItinerary(DESTINATIONS[key]);
      setLoading(false);
      setPage("dashboard");
    }, 5000);
  };

  return (
    <>
      <style>{styles}</style>

      {/* Navbar */}
      <nav className={`navbar${scrolled ? " scrolled" : ""}`}>
        <div className="nav-logo" onClick={() => navigate("home")}>
          <div className="nav-logo-icon">🧭</div>
          <span className="nav-logo-text">Rahbar</span>
        </div>
        <div className="nav-links">
          {["home","planner","dashboard","about"].map(p => (
            <button key={p} className={`nav-link${page===p?" active":""}`} onClick={() => navigate(p)}>
              {p === "home" ? "Home" : p === "planner" ? "Plan Trip" : p === "dashboard" ? "Dashboard" : "About Us"}
            </button>
          ))}
          <button className="nav-cta" onClick={() => navigate("planner")}>Plan My Trip</button>
        </div>
        <button className="hamburger" onClick={() => setMenuOpen(!menuOpen)} aria-label="Menu">
          <span/><span/><span/>
        </button>
      </nav>
      <div className={`mobile-menu${menuOpen?" open":""}`}>
        {["home","planner","dashboard","about"].map(p => (
          <button key={p} className="nav-link" onClick={() => navigate(p)}>
            {p==="home"?"Home":p==="planner"?"Plan Trip":p==="dashboard"?"Dashboard":"About Us"}
          </button>
        ))}
      </div>

      {/* Pages */}
      {page === "home" && <HomePage navigate={navigate} />}
      {page === "planner" && (loading
        ? <LoadingPage msg={loadingMessages[loadingMsg]} />
        : <PlannerPage onSubmit={handlePlanSubmit} />)}
      {page === "dashboard" && (itinerary
        ? <DashboardPage itinerary={itinerary} tripData={tripData} navigate={navigate} />
        : <EmptyDashboard navigate={navigate} />)}
      {page === "about" && <AboutPage />}

      <Footer navigate={navigate} />
    </>
  );
}

